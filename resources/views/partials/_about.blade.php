							<h2>Ciclo de vida Pagina Web</h2>
								<div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
									<div class="panel panel-default">
										<div class="panel-heading" role="tab" id="headingOne">
											<h4 class="panel-title">
												<a data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
													Analisis ,Planeacion del Sistema #1
												</a>
											</h4>
										</div>
										<div id="collapseOne" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingOne">
											<div class="panel-body">
												<p>Una vez que el cliente empieza a hablar de sus requerimientos, el equipo inicia la tarea del análisis. Como el sitio web será parte de un sistema, necesita un análisis completo sobre cómo la web o la aplicación web va a ayudar al sistema  y por ende al negocio. El primer asunto importante es el público objetivo. Todo el hardware, software, gente y datos presentes deben ser considerados desde el inicio del análisis. Por ejemplo, si la compañía ABC necesita un sitio web para tener online los detalles de recursos humanos, el equipo de análisis debe utilizar la data existente sobre los empleados de la base de datos actual. El análisis debe ser hecho de la manera que no tome demasiado tiempo pero sea informativo. El equipo debería determinar el costo-beneficio de manera realista pues sale del análisis. Para esto el analista debe consultar con los diseñadores, desarrolladores y testers para determinar un plan preciso.</p>

												<p><strong>Entrada:</strong> Entrevistas con clientes, correos y documentos de apoyo; notas de discusión, chat, conversaciones telefónicas, modelos de sitios/aplicaciones, etc.</p>

												<p><strong>Salida:</strong> 1. Plan de trabajo, 2. Costos involucrados, 3.Requerimientos del equipo, 4. Requerimientos de software y hardware, 5. Documentos de soporte y 6.Aprobación.</p>
											</div>
										</div>
									</div>
									<div class="panel panel-default">
										<div class="panel-heading" role="tab" id="headingTwo">
											<h4 class="panel-title">
												<a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
													Elaboración de las especificaciones #2
												</a>
											</h4>
										</div>
										<div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
											<div class="panel-body">
												<p>Las especificaciones preliminares se determinan cubriendo todos y cada uno de los requerimientos. Por ejemplo si el producto es un sitio web, los módulos del sitio que incluyen el diseño en general, la navegación y las partes dinámicas del sitio deben ser incluidas en las especificaciones. Proyectos más grandes requieren niveles mayores de consulta para evaluar los requerimientos técnicos y de negocios adicionales. Luego de revisar y aprobar el documento preliminar, una propuesta escrita es preparada, delineando el alcance del proyecto incluyendo responsabilidades, plazos y costos.</p>

												<p>Entrada: Reportes del equipo de análisis.</p>

												<p>Salida: Especificaciones y requerimientos completos a los individuos y al cliente o su representante.</p>
											</div>
										</div>
									</div>
									<div class="panel panel-default">
										<div class="panel-heading" role="tab" id="headingThree">
											<h4 class="panel-title">
												<a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
													Diseño y desarrollo #3
												</a>
											</h4>
										</div>
										<div id="collapseThree" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingThree">
											<div class="panel-body">
												<p>Luego de elaborar las especificaciones, el trabajo en el sitio web se programa al recibir la propuesta firmad, un depósito y cualquier contenido escrito o gráfico que deba ser incluido. En esta etapa la diagramación y navegación son diseñadas como prototipo.</p>
												<p>Alguno clientes pueden interesarse solo en un prototipo funcional, en ese caso debemos mostrarle la interactividad de la aplicación web o sitio. Pero en la mayoría de los casos el cliente suele interesarse al ver dos o tres diseños con imágenes y navegación.</p>
												<p>Puede haber muchas sugerencias y cambios del lado del cliente, y cualquier cambio debe ser probado antes de pasar a la siguiente etapa. Las revisiones pueden ser mostradas por la web al cliente para que las evalúe. Los comentarios del cliente, sus alcances y aprobaciones deben ser comunicadas vía e-mail, fax y teléfono.</p>
												<p>Durante la etapa del diseño, el equipo debe desarrollar planes de prueba y procedimientos para asegurarse de la calidad. Es necesario obtener la aprobación del cliente en diseño y planes de proyecto. En paralelo, el equipo de datos debe sentarse a pensar en los requerimientos y desarrollar la base de datos con toda su estructura y la data de muestra también debe ser preparada.</p>
												<p><strong>Entrada:</strong> Especificaciones y requerimientos.</p>
												<p><strong>Salida:</strong> Diseño web con plantillas, imágenes y prototipo.</p>

