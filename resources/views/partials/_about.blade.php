											<h4 class="panel-title">
												<a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapseSix" aria-expanded="false" aria-controls="collapseSix">
													Pruebas #6
												</a>
											</h4>
										</div>
										<div id="collapseSix" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSix">
											<div class="panel-body">
												<p>A diferencia del software, las aplicaciones web necesitan muchas pruebas, pues siempre funcionan como sistemas multiusuario con las limitaciones del ancho de banda. Algunas de las pruebas que deben ser hechas son la prueba de integración, de estrés, la prueba de escalabilidad, de carga, resolución y de navegador cruzado. Pruebas manuales y automáticas deben hacerse sin falla. Por ejemplo, es necesario probar la carga rápida de gráficos y calcular su tiempo de carga, pues es muy importante para cualquier sitio web. Hay algunas herramientas de prueba así como servicios online de prueba, que pueden ayudar al equipo a probar sus aplicaciones.</p>
												<p><strong>Entrada:</strong> El sitio, requerimientos y especificaciones, documentos de apoyo, especificaciones técnicas y documentos técnicos.</p>
												<p><strong>Salida:</strong> Aplicación/sitio completado, reportes de prueba,  registros de errores, interacción frecuente con desarrolladores y diseñadores.</p>
