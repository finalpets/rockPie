											</h4>
										</div>
										<div id="collapseFive" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFive">
											<div class="panel-body">
												<p>Ahora es turno de los <strong>programadores</strong> para agregar su código sin alterar el diseño. A diferencia del diseño tradicional, el desarrollador debe saber que la interfaz y el código no deben modificar el diseño ni la navegación. Si el sitio es dinámico entonces el código debe usar la plantilla. El desarrollador puede necesitar comunicarse con el diseñador, para poder entender el diseño. El diseñador puede necesitar algunos botones gráficos para lo que necesita al desarollador. Si trabaja un equipo de programadores deben usar un CVS para controlar sus fuentes. El equipo de código debe generar los planes de prueba necesarios así como la documentación técnica. Por ejemplo, usuarios de Java pueden usar JavaDoc para desarollar sus documentos para entender su flujo de código. La documentación para el usuario final también puede ser preparada por el equipo de código, la que puede ser utilizada por un escritor técnico que los entienda y escribir luego manuales de ayuda.</p>
												<p><strong>Entrada:</strong> El sitio con formularios y las especificaciones.</p>
												<p><strong>Salida:</strong> Funciones de base de datos controladas por  el sitio, documentos codificados.</p>
